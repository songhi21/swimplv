            <p class="textfontaboutmain text-left" ><h2>Rationale</h2> <br>Cauayan City in Isabela is one of the country’s premier agro-industrial hubs and also one of the Philippines’ first smart cities (Smart Communications, 2019), recognized by the Department of Science and Technology (DOST) for  its  innovation  in  adopting  science and technology development (Alvarez, M.S. et.al., 2020). Since urban areas, such as Cauayan City, are the hub of economic development and activity, there is a tendency for people and businesses to converge which results in higher water demand.

                At present, Cauayan has a total  fifteen (15) water pumping stations and eight (8) elevated water tanks serving only 12,067 households (29%) out of 30,207 households (71%).   The  combined distribution efficiency of these water infrastructures under  the management and supervision  of the Cauayan City Water District (CCWD) is only  87% which is accounted from the 9,229.63 m3 of metered water out of the distributed water of 10,518.13 m3 daily or with a combined distribution losses of   10% which is  1282.50m3 losses per day, ( Alvarez  M. S. et.al., 2020).  The said study  suggests  the  necessity to introduce new and innovative water management technologies and systems adapted to climate change to addressed the need of the city like; improve the  performance of the  existing water infrastructure systems,  lack of management tool  for  more efficient delivery of water services, limited service coverage of the water district due to limited water resource and depletion and contamination of aquifers and other water sources since shallow aquifer are mainly  utilized. Hence, a  GIS-based decision support tool in managing urban water infrastructure with storm water invention is a proposed solution  to address the need of the city.
                
                In order to maximize and properly utilize  water  infrastructure, the integration of decision support systems (DSS) and geographic information systems (GIS) is needed to provide the necessary spatial database for transforming a simple spatial query and visualization tool into a powerful analytical and spatially distributed modeling tool (Satti, S.R., et.al. 2004). Reitsma (1996) define a DSS for water resources applications as computer-based systems which integrate state information, dynamic or process information, and plan evaluation tools into a single software implementation.  One of the tools that can be used as DSS is the MIKE Operation. It is a complete GIS-integrated modelling tool that enables users to make sound decisions and create future concepts for urban stormwater systems  – concepts that are cost-effective as well as resilient to change <a style="color: blue" href="https://www.mikepoweredbydhi.com" target="_blank">www.mikepoweredbydhi.com</a>.</p>
