            <h2>Stormwater Infrastructure Systems</h2>
            <p>The stormwater water intervention facility has  a total catchment area of 3 hectares with a storage capacity  1750 cubic meter. Figure 5 shows the schematic diagram of the proposed stormwater infrastructure system with the different  components (i.e. Flushing  treatment device,  storage tank, groundwater recharge tank,  UV treatment , suction pumps , elevated tank,  water quality monitoring system and valves and pipings).The  recommendation to consider runoff water from the surface/roads aside from rainwater/stormwater  falling from the roof is not feasible because the design storage capacity of the stormwater is limited  only  to  one (1) hour rainfall duration   which is enough to fill the storage tank. On the other hand, elevating the water storage tank in the proposed design and system is very much appreciated. However, the said recommendation is not feasible considering that  the building structures  in the proposed site were not uniform in terms of number of storey (I.e.  BJMP and PNP buildings   are 4 storey buildings  while others are two storey  and one storey buildings). Elevating the storage tank with this physical condition still needs  motor pumps and also requires heavy  foundation structures. In addition, the space of the proposed site  had limited space. Hence, the space occupied by the  proposed subsurface storage tank when covered will be useful for other purposes. Nevertheless,  the recommendations will  still be considered as a possible option when scaled up to other areas.</p>
